const arr = [1, 2];
arr.unshift(0); // 返回值为 3，即新的数组长度
// arr 现在为 [0, 1, 2]

arr.unshift(-2, -1); // 返回值为 5
// arr 现在为 [-2, -1, 0, 1, 2]

arr.unshift([-4, -3]); // 返回值为 6
// arr 现在为 [[-4, -3], -2, -1, 0, 1, 2]

arr.unshift([-7, -6], [-5]); // 返回值为 8
// arr 现在为 [ [-7, -6], [-5], [-4, -3], -2, -1, 0, 1, 2 ]
