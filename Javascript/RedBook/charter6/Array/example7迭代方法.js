// 迭代方法 ECMAScript为数组定义了5个迭代方法。每个方法接收两个参数：以每 一项为参数运行的函数，以及可选的作为函数运行上下文的作用域对象 （影响函数中this的值）。传给每个方法的函数接收3个参数：数组元 素、元素索引和数组本身。因具体方法而异，这个函数的执行结果可能 会也可能不会影响方法的返回值。数组的5个迭代方法如下。 every()：对数组每一项都运行传入的函数，如果对每一项函数都 返回true，则这个方法返回true。 filter()：对数组每一项都运行传入的函数，函数返回true的项会 组成数组之后返回。 forEach()：对数组每一项都运行传入的函数，没有返回值。 map()：对数组每一项都运行传入的函数，返回由每次函数调用的 结果构成的数组。 some()：对数组每一项都运行传入的函数，如果有一项函数返回



